void adc_scan(){
	ADC_Select_CH(ADC_CHANNEL_1);
	HAL_ADC_Start(&hadc1);
	HAL_ADC_PollForConversion(&hadc1, 1000);
	int temp_val = HAL_ADC_GetValue(&hadc1);
	if(temp_val < 3800){
		measure_temp = measure_temp * 0.9 + HAL_ADC_GetValue(&hadc1) * 0.1;
	}
	HAL_ADC_Stop(&hadc1);
	ADC_Select_CH(ADC_CHANNEL_2);
	HAL_ADC_Start(&hadc1);
	HAL_ADC_PollForConversion(&hadc1, 1000);
	measure_vibration = HAL_ADC_GetValue(&hadc1);
	HAL_ADC_Stop(&hadc1);
	ADC_Select_CH(ADC_CHANNEL_3);
	HAL_ADC_Start(&hadc1);
	HAL_ADC_PollForConversion(&hadc1, 1000);
	measure_temp_set = measure_temp_set * 0.9 + HAL_ADC_GetValue(&hadc1) * 0.1;
	HAL_ADC_Stop(&hadc1);
}
void HAL_TIM_OC_DelayElapsedCallback(TIM_HandleTypeDef *htim){
	if(htim == &htim2){
		if(htim->Channel == HAL_TIM_ACTIVE_CHANNEL_2){
			cnt++;
			adc_scan();
			//calculate temperature
			temp_set = (float)measure_temp_set / 4096 * 200 + 200;
			temp_real = measure_temp * 0.1238 + 17.2814;
			//pid=>pwm
			err = temp_set - temp_real;
			if(err <= 0) pwm = 0;
			else pwm = kp * err + kd * (err - prev_err) + ki * sum_err;
			if(pwm > 1000) pwm = 1000;
			if(pwm < 0) pwm = 0;
			if(state == 0) pwm = 0;
			prev_err = err;
			//pwm = (float)measure_temp_set / 4096 * 1800;
			TIM2->CCR1 = pwm;
			
			//sense vibration
			if(measure_vibration > 100){ //threshold
				cnt = 0;
				state = 1;
			}else if(cnt > 60000){ //1 min no movement
				state = 0;
			}

		}
	}
}


